package part1.ch02.code;

public class Sec02 {
    public static void main(String[] args) {
        // 1 문자열 변수
        String food;
        food = "치킨";
        System.out.println(food);    // "치킨" 출력

        // 2 정수형 변수
        int age;
        age = 21;
        System.out.println(age);     // "21" 출력

        // 3 실수형 변수
        double weight;
        weight = 73.63;
        System.out.println(weight);    // "73.63" 출력

        // 4 논리형 변수
        boolean hungry;
        hungry = false;
        System.out.println(hungry);    // "false" 출력
    }
}
